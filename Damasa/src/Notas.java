    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        q.add(new Nodo(txtTitulo.getText()));

        String u = txtTitulo.getText();
        String n = txaTexto.getText();
        FileWriter flwr = null;
        BufferedWriter bfwr = null;
        File file = new File("C:\\Users\\martin\\Documents\\NetBeansProjects\\Damasa\\notas\\notas.txt");
        try {
            ObjectOutputStream guar = new ObjectOutputStream(new FileOutputStream(u));
            guar.writeObject(u);
            guar.writeObject(n);
            flwr = new FileWriter("C:\\Users\\martin\\Documents\\NetBeansProjects\\Damasa\\notas\\notas.txt");
            bfwr = new BufferedWriter(flwr);
            for (int i = 0; i < q.size(); i++) {
                bfwr.write(q.getAt(i));
                bfwr.newLine();
            }
            bfwr.close();
            guar.close();
            JOptionPane.showMessageDialog(null, "Se ha guardado la nota con exito");

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No se ha guardado la nota");
            Logger.getLogger(Notas.class.getName()).log(Level.SEVERE, null, ex);
        }
        txaTexto.setText(null);
        txtTitulo.setText(null);
        txtTitulo.setVisible(false);
        txaTexto.setVisible(false);
        btnGuardar.setVisible(false);
        txaP.setText(q.print2());
    }//GEN-LAST:event_btnGuardarActionPerformed

