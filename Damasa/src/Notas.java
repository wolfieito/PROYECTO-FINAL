    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        String tit = txtTitulo.getText();
        File file = new File("C:\\Users\\martin\\Documents\\NetBeansProjects\\Damasa\\" + tit);
        boolean borrar = false;
        try {
            ObjectInputStream ent = new ObjectInputStream(new FileInputStream(tit));

            Object u = ent.readObject();

            if (tit.equals(u)) {
                ent.close();//tienes que cerrar el archivo para borrarlo
                if (file.delete()) {
                    btnBorrar.setVisible(false);
                    JOptionPane.showMessageDialog(null, "Se ha borrado la nota con exito");
                    borrar = true;
                    txtTitulo.setVisible(false);
                }
            } else {
                JOptionPane.showMessageDialog(null, "No existe nota con ese titulo");
            }
            ent.close();
        } catch (IOException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, "No existe nota con ese titulo");
        }
        if(borrar){
            try {
                FileReader fileR = new FileReader("C:\\Users\\martin\\Documents\\NetBeansProjects\\Damasa\\notas\\notas.txt");
                BufferedReader debR = new BufferedReader(fileR);
                String linea;
                int i = 0;
                while ((linea = debR.readLine()) != null) {
                    if (linea.equals(tit)) {
                        q.deleteAt(i);
                        break;
                    } else {
                        i++;
                    }
                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Notas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Notas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(Notas.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            try {
            FileWriter flwr = new FileWriter("C:\\Users\\martin\\Documents\\NetBeansProjects\\Damasa\\notas\\notas.txt");
            BufferedWriter bfwr = new BufferedWriter(flwr);
            for (int i = 0; i < q.size(); i++) {
                bfwr.write(q.getAt(i));
                bfwr.newLine();
            }
            bfwr.close();

        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, "No se ha guardado la nota");
            Logger.getLogger(Notas.class.getName()).log(Level.SEVERE, null, ex);
        }
            if(q.size()!=0)
            txaP.setText(q.print2());
            else
                txaP.setText(null);
        }
        txtTitulo.setText(null);
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String titu = txtTitulo.getText();
        try {
            ObjectInputStream ent = new ObjectInputStream(new FileInputStream(titu));

            Object u = ent.readObject();
            Object n = ent.readObject();

            if (titu.equals(u)) {
                txaTexto.setVisible(true);
                txaTexto.setText((String) n);
                ent.close();
                JOptionPane.showMessageDialog(null, "Nota encontrado con exito");
            } else {
                JOptionPane.showMessageDialog(null, "No existe nota con ese titulo");
            }
        } catch (IOException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(null, "No existe nota con ese titulo");
        }
        btnBuscar.setVisible(false);
    }//GEN-LAST:event_btnBuscarActionPerformed

